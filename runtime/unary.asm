; *******************************************************************************************
;
;								Increment/Decrement
;
; *******************************************************************************************

Inc_Unary: 	;; INC 	$F0
		inc16 	Reg16
		jmp 	ExecLoop

Dec_Unary: 	;; DEC 	$F1
		dec16 	Reg16
		jmp 	ExecLoop

; *******************************************************************************************
;
;						Shifts - these are logical unsigned not arithmetic
;
; *******************************************************************************************

Shl_Unary:	;; SHL $F2
		asl 	Reg16
		rol 	Reg16+1
		jmp 	ExecLoop

Shr_Unary:	;; SHR $F3
		lsr 	Reg16+1
		ror 	Reg16
		jmp 	ExecLoop

; *******************************************************************************************
;
;						Clear, assuming we using constant 0 a fair bit :)
;
; *******************************************************************************************

Clr_Unary:	;; CLR $F4
		lda 	#0
		sta		Reg16
		sta 	Reg16+1
		jmp 	ExecLoop
		
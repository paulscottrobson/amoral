;
;									Print Character
;
; *******************************************************************************************

		define 	"print.character:1",EndPrintCharacter
PrintCharacter:
		jsr 	$FFD2
		rts

EndPrintCharacter:

; *******************************************************************************************
;
;									Read 60Hz Clock.
;
; *******************************************************************************************

		define 	"read.timer:0",EndReadTimer
ReadTimer:
		jsr 	$FFDE
		rts

EndReadTimer:


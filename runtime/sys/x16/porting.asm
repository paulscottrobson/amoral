; *******************************************************************************************
;
;									Print Character
;
; *******************************************************************************************

		define 	"print.character:1",EndPrintCharacter
PrintCharacter:
		jsr 	$FFD2
		rts
EndPrintCharacter:

; *******************************************************************************************
;
;									Print CR/LF
;
; *******************************************************************************************

		define 	"print.crlf:0",EndPrintCRLF
		lda 	#13
		jmp 	PrintCharacter
EndPrintCRLF:

; *******************************************************************************************
;
;									Read 60Hz Clock.
;
; *******************************************************************************************

		define 	"read.timer:0",EndReadTimer
ReadTimer:
		jsr 	$FFDE						; the documentation is mostly wrong it's YXA.
		rts

EndReadTimer:

